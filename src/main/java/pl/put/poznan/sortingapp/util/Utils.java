package pl.put.poznan.sortingapp.util;

/**
 * Klasa dodatkowa, Utils.
 */
public class Utils {
    /**
     * Wypisuje liste obiektow.
     * @param arr Ciag obiektow do wypisania.
     */
    public static void printArray(int[] arr) {
        for (int j : arr) System.out.print(j + " ");
        System.out.println();
    }
}
